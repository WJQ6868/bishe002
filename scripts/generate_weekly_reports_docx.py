def set_run_font(run, font_name: str, size: float, bold: bool = False) -> None:
    run.font.name = font_name
    run._element.rPr.rFonts.set(qn("w:eastAsia"), font_name)
    run.font.size = Pt(size)
    run.bold = bold


def add_labeled_paragraph(doc: Document, label: str, text: str) -> None:
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Cm(0.74)
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.space_after = Pt(4)
    r1 = p.add_run(f"{label}：")
    set_run_font(r1, "黑体", 12, bold=True)
    r2 = p.add_run(text)
    set_run_font(r2, "宋体", 12)


def build_doc() -> Document:
    doc = Document()
    section = doc.sections[0]
    section.top_margin = Cm(2.54)
    section.bottom_margin = Cm(2.54)
    section.left_margin = Cm(3.0)
    section.right_margin = Cm(2.5)

    normal = doc.styles["Normal"]
    normal.font.name = "宋体"
    normal._element.rPr.rFonts.set(qn("w:eastAsia"), "宋体")
    normal.font.size = Pt(12)

    title = doc.add_paragraph()
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    title.paragraph_format.space_after = Pt(18)
    tr = title.add_run("AI赋能的高校教务系统\n16篇周记")
    set_run_font(tr, "黑体", 16, bold=True)

    intro = doc.add_paragraph()
    intro.paragraph_format.first_line_indent = Cm(0.74)
    intro.paragraph_format.line_spacing = 1.5
    intro_run = intro.add_run(
        "说明：以下周记依据当前项目代码、数据库样例、部署脚本、测试结果以及论文整理过程进行梳理，"
        "内容围绕项目真实开发轨迹展开，重点突出基础教务平台建设与 AI 客服、AI 课程助手、智能教案等核心功能的实现过程。"
    )
    set_run_font(intro_run, "宋体", 12)

    for idx, entry in enumerate(ENTRIES):
        if idx > 0:
            doc.add_paragraph().add_run().add_break(WD_BREAK.PAGE)

        heading = doc.add_paragraph()
        heading.paragraph_format.space_before = Pt(6)
        heading.paragraph_format.space_after = Pt(8)
        hr = heading.add_run(entry["title"])
        set_run_font(hr, "黑体", 14, bold=True)

        add_labeled_paragraph(doc, "本周工作", entry["work"])
        add_labeled_paragraph(doc, "问题与调整", entry["issues"])
        add_labeled_paragraph(doc, "下周计划", entry["plan"])

    return doc


def main() -> None:
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    BACKUP_PATH.parent.mkdir(parents=True, exist_ok=True)
    doc = build_doc()
    doc.save(OUTPUT_PATH)
    doc.save(BACKUP_PATH)
    print(f"saved: {OUTPUT_PATH}")
    print(f"backup: {BACKUP_PATH}")


if __name__ == "__main__":
    main()
